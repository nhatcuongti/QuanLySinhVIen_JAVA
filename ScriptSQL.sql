USE ManageStudent
GO

CREATE TABLE STUDENT(
	ID VARCHAR(200) PRIMARY KEY,
	NAME NVARCHAR(200),
	GPA FLOAT,
	ADDR NVARCHAR(300),
	NOTE NVARCHAR(300),
	IMG VARCHAR(100)
)
GO

CREATE PROCEDURE insertData
	@ID VARCHAR(200),
	@NAME NVARCHAR(200),
	@GPA FLOAT,
	@ADDR NVARCHAR(300),
	@NOTE NVARCHAR(300),
	@IMG VARCHAR(100)
AS
BEGIN
	INSERT INTO STUDENT
	VALUES(@ID, @NAME, @GPA, @ADDR, @NOTE, @IMG)
END
GO

select * from STUDENT